#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;
	char	*a;

	i = 0;
	a = (char *)s;
	while (i < n)
	{
		if ((unsigned char)a[i] == (unsigned char)c)
			return ((char *)s + i);
		i++;
	}
	return (NULL);
}
