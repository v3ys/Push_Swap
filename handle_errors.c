int	error_syntax(char *str_n)
{
	if (!(*str_n == '+'
			|| *str_n == '-'
			|| (*str_n >= '0' && *str_n <= '9')))
		return (1);
	if ((*str_n == '+'
			|| *str_n == '-')
		&& !(str_n[1] >= '0' && str_n[1] <= '9'))
		return (1);
	while (*++str_n)
	{
		if (!(*str_n >= '0' && *str_n <= '9'))
			return (1);
	}
	return (0);
}

int	duplicate_error(t_stack_node *a, int n)
{
	if (!a)
		return (0);
	while (a)
	{
		if (a->nbr == n)
			return (1);
		a = a->next;
	}
	return (0);
}

void	free_stack(t_stack_node **stack, char **argv, int check)
{
	t_stack_node	*tmp;
	t_stack_node	*current;
	int				i;
	
	i = 0;
	if (!stack)
		return ;
	current = *stack;
	while (current)
	{
		tmp = current->next;
		current->nbr = 0;
		free(current);
		current = tmp;
	}
	if (check == 1)
	{
		while(argv[i])
		{
			free(argv[i]);
			i++;
		}
		free (argv);
	}
	*stack = NULL;
}

void	free_errors(t_stack_node **a, char **argv, int check)
{

	free_stack(a, argv, check);
	ft_printf("Error\n");
	exit(1);
}
